#!/usr/bin/env node
/**
 * Scenario 02: BEGIN_CHECKOUT
 * Trigger: GET /checkout via links-redirect
 * Verifies: 302 redirect, event in D1, client_ip captured, sGTM via replay
 */
import { loadEnv, applyEnv, requireEnv, DEFAULT_ENV_FILE, parseScenarioArgs } from "../lib/config.mjs";
import { d1Query, waitForRow, sqlEscape, sleep } from "../lib/d1.mjs";
import { getUrl } from "../lib/http.mjs";
import { replayApply } from "../lib/replay.mjs";
import { step, skipStep, assertPayloadJson, printResult, printSummary } from "../lib/assert.mjs";

export const SCENARIO_ID = "02";
export const SCENARIO_NAME = "02-begin-checkout";
export const TAGS = ["ingress", "identity", "tracking", "sgtm"];

export async function run(opts = {}) {
  const ts = Date.now();
  const envFile = opts.envFile || DEFAULT_ENV_FILE;
  const env = loadEnv(envFile);
  applyEnv(env);
  requireEnv(["CLOUDFLARE_API_TOKEN", "CLOUDFLARE_ACCOUNT_ID"]);

  const links_url = process.env.DECOLE_LINKS_URL || "https://links.decolesuacarreiraesg.com.br";
  const anonymousId = `anon-e2e-02-${ts}`;
  const fbp = `fb.1.${ts}.checkout`;
  const utmSource = "e2e-test";
  const metaTestEventCode = opts.metaTestEventCode || process.env.META_TEST_EVENT_CODE_DECOLE_ESG || "";

  const start = Date.now();
  const steps = [];

  // Step 1: GET /checkout → 302
  let eventId = null;
  steps.push(await step("redirect_302", async () => {
    const res = await getUrl(`${links_url}/checkout`, { anonymous_id: anonymousId, fbp, utm_source: utmSource });
    if (res.status !== 302) throw new Error(`expected 302 got ${res.status}`);
    if (!res.location?.includes("hotmart") && !res.location?.includes("pay.")) {
      throw new Error(`unexpected redirect location: ${res.location}`);
    }
    return `→ ${res.location?.slice(0, 60)}...`;
  }));

  if (steps[0].status === "fail") return finalize(SCENARIO_NAME, steps, start);

  // Wait a bit for queue processing
  await sleep(5000);

  // Step 2: Find BEGIN_CHECKOUT in D1 (by anonymous_id — event_id is generated by links-redirect)
  let eventRow = null;
  steps.push(await step("event_in_d1", async () => {
    eventRow = await waitForRow(
      "decole-d1-event-store",
      `SELECT event_id, event_type, source, anonymous_id, profile_id, payload_json FROM funnel_events WHERE anonymous_id='${sqlEscape(anonymousId)}' AND event_type='BEGIN_CHECKOUT' ORDER BY occurred_at DESC LIMIT 1`,
      (r) => r.event_type === "BEGIN_CHECKOUT",
      { timeout: 60000, poll: 3000, description: `BEGIN_CHECKOUT anon=${anonymousId}` }
    );
    eventId = eventRow.event_id;
    if (eventRow.source !== "site") throw new Error(`expected source=site got ${eventRow.source}`);
    return `event_id=${eventId} source=${eventRow.source}`;
  }));

  // Step 3: identity_links
  steps.push(await step("identity_resolved", async () => {
    if (!eventRow?.profile_id) throw new Error("profile_id not set in funnel_events");
    const rows = d1Query(
      "decole-d1-identity",
      `SELECT profile_id, anonymous_id FROM identity_links WHERE anonymous_id='${sqlEscape(anonymousId)}' LIMIT 1`
    );
    if (!rows[0]?.profile_id) throw new Error(`no identity_links row for anonymous_id=${anonymousId}`);
    return `profile_id=${rows[0].profile_id}`;
  }));

  // Step 4: payload_json contains fbp
  steps.push(await step("payload_has_fbp", async () => {
    const payload = assertPayloadJson(eventRow, { fbp: true });
    if (payload.fbp !== fbp) throw new Error(`expected fbp=${fbp} got ${payload.fbp}`);
    return `fbp=${payload.fbp}`;
  }));

  // Step 5: sGTM via replay
  if (!opts.skipSgtm) {
    steps.push(await step("sgtm_replay", async () => {
      if (!eventId) throw new Error("no event_id to replay");
      const result = await replayApply(eventId, { metaTestEventCode, envFile });
      if (!result.planned.sgtm) throw new Error("replay planned.sgtm=false (missing tracking config)");
      if (!result.sent.includes("sgtm")) throw new Error("replay did not send to sgtm");
      return `sent=${result.sent.join(",")} meta_test_code=${result.meta_test_event_code_applied}`;
    }));
  } else {
    steps.push(skipStep("sgtm_replay", "skipSgtm=true"));
  }

  return finalize(SCENARIO_NAME, steps, start);
}

function finalize(name, steps, start) {
  const status = steps.some((s) => s.status === "fail") ? "fail" : "pass";
  return { scenario: name, status, elapsed_ms: Date.now() - start, steps };
}

if (process.argv[1].endsWith("02-begin-checkout.mjs")) {
  const result = await run(parseScenarioArgs());
  console.log(`\n[${SCENARIO_NAME}]`);
  result.steps.forEach(printResult);
  printSummary(result);
  console.log("\n" + JSON.stringify(result, null, 2));
  process.exit(result.status === "pass" ? 0 : 1);
}
